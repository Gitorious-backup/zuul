module Zuul
  class Error
    attr_reader :status, :message

    def initialize(status, message)
      @status = status
      @message = message
    end

    def type
      underscore(self.class.name).sub(/^zuul\//, "")
    end

    private
    def underscore(str)
      str.gsub(/::/, '/').
        gsub(/([A-Z]+)([A-Z][a-z])/,'\1_\2').
        gsub(/([a-z\d])([A-Z])/,'\1_\2').
        tr("-", "_").
        downcase
    end
  end

  class NotFoundError < Error
    def initialize(message)
      super(404, message)
    end
  end
end
